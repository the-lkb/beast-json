static void from_rapidjson(const rapidjson::Value &v, MarketTick &t) {
    t.sym    = v["sym"].GetString();
    t.bid    = v["bid"].GetDouble();
    t.ask    = v["ask"].GetDouble();
    t.bid_sz = v["bid_sz"].GetInt64();
    t.ask_sz = v["ask_sz"].GetInt64();
    t.ts_ns  = v["ts_ns"].GetInt64();
}
static void from_rapidjson(const rapidjson::Value &v, Order &o) {
    o.id    = v["id"].GetString();
    o.sym   = v["sym"].GetString();
    o.side  = v["side"].GetString();
    o.px    = v["px"].GetDouble();
    o.qty   = v["qty"].GetInt64();
    o.type  = v["type"].GetString();
    o.ts_ns = v["ts_ns"].GetInt64();
}
static void from_rapidjson(const rapidjson::Value &v, Trade &t) {
    t.sym       = v["sym"].GetString();
    t.px        = v["px"].GetDouble();
    t.qty       = v["qty"].GetInt64();
    t.aggressor = v["aggressor"].GetString();
    t.ts_ns     = v["ts_ns"].GetInt64();
}
static void from_rapidjson(const rapidjson::Value &v, BookLevel &bl) {
    bl.px  = v["px"].GetDouble();
    bl.sz  = v["sz"].GetInt64();
    bl.cnt = v["cnt"].GetInt64();
}
static void from_rapidjson(const rapidjson::Value &v, BookSnapshot &bs) {
    bs.sym   = v["sym"].GetString();
    bs.ts_ns = v["ts_ns"].GetInt64();
    const auto &bids = v["bids"];
    for (unsigned i = 0; i < 5 && i < bids.Size(); ++i)
        from_rapidjson(bids[i], bs.bids[i]);
    const auto &asks = v["asks"];
    for (unsigned i = 0; i < 5 && i < asks.Size(); ++i)
        from_rapidjson(asks[i], bs.asks[i]);
}

// ─────────────────────────────────────────────────────────────────────────────
// simdjson helpers — on-demand field extraction for MarketTick
// simdjson shines on parse throughput; for full struct extraction it needs
// manual key iteration (shown here for a fair comparison on MarketTick).
// ─────────────────────────────────────────────────────────────────────────────

static bool simdjson_extract(simdjson::simdjson_result<simdjson::ondemand::document> &&res, MarketTick &t) {
    auto obj = res.get_object();
    for (auto field : obj) {
        std::string_view key = field.unescaped_key().value_unsafe();
        if      (key == "sym")    t.sym    = std::string(field.value().get_string().value_unsafe());
        else if (key == "bid")    t.bid    = field.value().get_double().value_unsafe();
        else if (key == "ask")    t.ask    = field.value().get_double().value_unsafe();
        else if (key == "bid_sz") t.bid_sz = field.value().get_int64().value_unsafe();
        else if (key == "ask_sz") t.ask_sz = field.value().get_int64().value_unsafe();
        else if (key == "ts_ns")  t.ts_ns  = field.value().get_int64().value_unsafe();
    }
    return true;
}

// ─────────────────────────────────────────────────────────────────────────────
// Latency statistics (P50 / P95 / P99 / P99.9)
// ─────────────────────────────────────────────────────────────────────────────

struct LatStats {
    double mean, p50, p95, p99, p999, tmax;
    size_t alloc_kb;

    static LatStats from(std::vector<double> &v, size_t kb) {
        std::sort(v.begin(), v.end());
        size_t n = v.size();
        double sum = 0; for (double x : v) sum += x;
        return { sum/n,
                 v[n*50/100], v[n*95/100],
                 v[n*99/100], v[n*999/1000],
                 v.back(), kb };
    }

    void print(const char *lib) const {
        printf("  %-16s | %6.0f | %6.0f | %6.0f | %6.0f | %7.0f | %4zu KB\n",
               lib, mean, p50, p95, p99, p999, alloc_kb);
    }
};

static constexpr size_t WARMUP  = 5'000;
static constexpr size_t SAMPLES = 100'000;

template <typename Fn>
static std::vector<double> sample(Fn &&fn) {
    std::vector<double> v;
    v.reserve(SAMPLES);
    for (size_t i = 0; i < WARMUP; ++i) fn();
    for (size_t i = 0; i < SAMPLES; ++i) {
        auto t0 = std::chrono::high_resolution_clock::now();
        fn();
        auto t1 = std::chrono::high_resolution_clock::now();
        v.push_back(std::chrono::duration<double, std::nano>(t1 - t0).count());
    }
    return v;
}

static void print_lat_header(const char *msg_type, size_t bytes) {
    printf("\n┌─ %-36s %3zu bytes ────────────────────────────\n",
           msg_type, bytes);
    printf("│  %-16s │  mean │   p50 │   p95 │   p99 │   p99.9 │   alloc\n",
           "library");
    printf("│  %s\n", std::string(72, '-').c_str());
}

// ─────────────────────────────────────────────────────────────────────────────
// Per-message-type benchmark
// ─────────────────────────────────────────────────────────────────────────────

template <typename T>
static void bench_msg(const char *label, const std::string &json,
                      bool include_simdjson = false) {
    print_lat_header(label, json.size());

    // ── Beast Nexus (zero-tape, zero-alloc hot path) ──────────────────────
    {
        size_t rss0 = bench::get_current_rss_kb();
        { T tmp = beast::fuse<T>(json); bench::do_not_optimize(tmp); }
        size_t kb = bench::get_current_rss_kb() - rss0;

        auto v = sample([&] {
            T tmp = beast::fuse<T>(json);
            bench::do_not_optimize(tmp);
        });
        LatStats::from(v, kb).print("Beast Nexus");
    }

    // ── Beast DOM with document reuse (tape buffer reused across calls) ──
    {
        beast::Document doc;
        // cold parse to size the tape
        { auto r = beast::parse(doc, json); T tmp{}; beast::from_json(r,tmp); }

        size_t rss0 = bench::get_current_rss_kb();
        { auto r = beast::parse(doc, json); T tmp{}; beast::from_json(r,tmp); }
        size_t kb = bench::get_current_rss_kb() - rss0;

        auto v = sample([&] {
            auto r = beast::parse(doc, json);   // tape buffer reused
            T tmp{};
            beast::from_json(r, tmp);
            bench::do_not_optimize(tmp);
        });
        LatStats::from(v, kb).print("Beast DOM");
    }

#ifdef BEAST_HAS_GLAZE
    // ── Glaze ──────────────────────────────────────────────────────────────
    {
        size_t rss0 = bench::get_current_rss_kb();
        { T tmp{}; auto ec = glz::read_json(tmp, json); (void)ec; bench::do_not_optimize(tmp); }
        size_t kb = bench::get_current_rss_kb() - rss0;

        auto v = sample([&] {
            T tmp{};
            auto ec = glz::read_json(tmp, json);
            (void)ec;
            bench::do_not_optimize(tmp);
        });
        LatStats::from(v, kb).print("Glaze");
    }
#endif

    // ── simdjson (on-demand, manual field extraction) ─────────────────────
    if (include_simdjson) {
        simdjson::ondemand::parser parser;
        // Pre-pad once; in production systems the buffer would be pre-allocated
        simdjson::padded_string padded{json};

        size_t rss0 = bench::get_current_rss_kb();
        { MarketTick tmp{}; simdjson_extract(parser.iterate(padded), tmp); bench::do_not_optimize(tmp); }
        size_t kb = bench::get_current_rss_kb() - rss0;

        auto v = sample([&] {
            MarketTick tmp{};
            simdjson_extract(parser.iterate(padded), tmp);
            bench::do_not_optimize(tmp);
        });
        LatStats::from(v, kb).print("simdjson");
    }

    // ── RapidJSON ──────────────────────────────────────────────────────────
    {
        size_t rss0 = bench::get_current_rss_kb();
        { rapidjson::Document d; d.Parse(json.c_str(), json.size()); T tmp{}; if(!d.HasParseError()) from_rapidjson(d,tmp); bench::do_not_optimize(tmp); }
        size_t kb = bench::get_current_rss_kb() - rss0;

        auto v = sample([&] {
            rapidjson::Document d;
            d.Parse(json.c_str(), json.size());
            T tmp{};
            if (!d.HasParseError()) from_rapidjson(d, tmp);
            bench::do_not_optimize(tmp);
        });
        LatStats::from(v, kb).print("RapidJSON");
    }

    // ── nlohmann ──────────────────────────────────────────────────────────
    {
        size_t rss0 = bench::get_current_rss_kb();
        { try { auto tmp = nlohmann::json::parse(json).get<T>(); bench::do_not_optimize(tmp); } catch(...){} }
        size_t kb = bench::get_current_rss_kb() - rss0;

        auto v = sample([&] {
            try {
                auto tmp = nlohmann::json::parse(json).get<T>();
                bench::do_not_optimize(tmp);
            } catch (...) {}
        });
        LatStats::from(v, kb).print("nlohmann");
    }

    printf("└  (all times in nanoseconds)\n");
}

// ─────────────────────────────────────────────────────────────────────────────
// Throughput: sustained messages per second
// ─────────────────────────────────────────────────────────────────────────────

template <typename T>
static void bench_throughput(const char *label, const std::string &json,
                              size_t iters = 2'000'000) {
    printf("\n  %-36s  %zu bytes\n", label, json.size());

    auto run = [&](const char *lib, auto fn) {
        bench::Timer t; t.start();
        fn();
        double ns_per = t.elapsed_ns() / iters;
        printf("    %-16s  %5.1f ns/msg   %5.1f M msgs/sec\n",
               lib, ns_per, 1000.0 / ns_per);
    };

    // Beast Nexus
    run("Beast Nexus", [&] {
        for (size_t i = 0; i < iters; ++i) {
            T tmp = beast::fuse<T>(json);
            bench::do_not_optimize(tmp);
        }
    });

#ifdef BEAST_HAS_GLAZE
    run("Glaze", [&] {
        for (size_t i = 0; i < iters; ++i) {
            T tmp{};
            auto ec = glz::read_json(tmp, json);
            (void)ec;
            bench::do_not_optimize(tmp);
        }
    });
#endif

    {
        simdjson::ondemand::parser parser;
        simdjson::padded_string padded{json};
        // simdjson throughput: parse-only (no struct extraction overhead)
        run("simdjson (parse)", [&] {
            for (size_t i = 0; i < iters; ++i) {
                auto doc = parser.iterate(padded);
                // Force full evaluation via ts_ns field
                int64_t ts; doc["ts_ns"].get(ts);
                bench::do_not_optimize(ts);
            }
        });
    }

    run("RapidJSON", [&] {
        for (size_t i = 0; i < iters; ++i) {
            rapidjson::Document d;
            d.Parse(json.c_str(), json.size());
            T tmp{};
            if (!d.HasParseError()) from_rapidjson(d, tmp);
            bench::do_not_optimize(tmp);
        }
    });

    run("nlohmann", [&] {
        for (size_t i = 0; i < iters; ++i) {
            try {
                auto tmp = nlohmann::json::parse(json).get<T>();
                bench::do_not_optimize(tmp);
            } catch (...) {}
        }
    });
}

// ─────────────────────────────────────────────────────────────────────────────
// Serialize throughput: how fast can we encode outgoing messages?
// Critical for order entry gateways and risk broadcast systems.
// ─────────────────────────────────────────────────────────────────────────────

template <typename T>
static void bench_serialize_throughput(const char *label, const T &obj,
                                       size_t iters = 2'000'000) {
    printf("\n  %-36s\n", label);
    std::string buf;
    buf.reserve(512);

    auto run_s = [&](const char *lib, auto fn) {
        bench::Timer t; t.start();
        fn();
        double ns_per = t.elapsed_ns() / iters;
        printf("    %-16s  %5.1f ns/msg   %5.1f M msgs/sec\n",
               lib, ns_per, 1000.0 / ns_per);
    };

    run_s("Beast write_to", [&] {
        for (size_t i = 0; i < iters; ++i) {
            buf.clear();
            beast::write_to(buf, obj);
            bench::do_not_optimize(buf);
        }
    });

#ifdef BEAST_HAS_GLAZE
    run_s("Glaze", [&] {
        for (size_t i = 0; i < iters; ++i) {
            buf.clear();
            auto ec = glz::write_json(obj, buf);
            (void)ec;
            bench::do_not_optimize(buf);
        }
    });
#endif

    run_s("RapidJSON", [&] {
        rapidjson::StringBuffer sbuf;
        for (size_t i = 0; i < iters; ++i) {
            sbuf.Clear();
            rapidjson::Writer<rapidjson::StringBuffer> w(sbuf);
            rapidjson::Document d;
            rapidjson::Value v;
            to_json(v, obj, d.GetAllocator());
            v.Accept(w);
            bench::do_not_optimize(sbuf);
        }
    });

    run_s("nlohmann", [&] {
        for (size_t i = 0; i < iters; ++i) {
            buf = nlohmann::json(obj).dump();
            bench::do_not_optimize(buf);
        }
    });
}

