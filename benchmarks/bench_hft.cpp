// benchmarks/bench_hft.cpp
//
// beast-json HFT (High-Frequency Trading) Benchmark
// ==================================================
// Demonstrates beast-json suitability for latency-critical financial systems:
//
//   • Sub-microsecond parse latency for common trading messages
//   • ZERO heap allocation via beast::fuse<T>() (Nexus Fusion Core)
//   • Latency distribution: P50 / P95 / P99 / P99.9 / max
//   • Message throughput (M msgs/sec)
//   • Document reuse (parse_reuse) for the DOM path
//
// Competitors: Beast Nexus | Beast DOM | Glaze | simdjson | RapidJSON | nlohmann
//
// Typical HFT message types covered:
//   L1 MarketTick   — highest frequency, ~100 bytes
//   Order           — new/cancel/modify, ~120 bytes
//   Trade/Fill      — execution report, ~90 bytes
//   L2 BookSnapshot — top-5 bid/ask ladder, ~350 bytes

