static void to_json(rapidjson::Value &v, const MarketTick &t,
                    rapidjson::Document::AllocatorType &a) {
    v.SetObject();
    v.AddMember("sym",    rapidjson::Value(t.sym.c_str(), a).Move(), a);
    v.AddMember("bid",    t.bid, a);
    v.AddMember("ask",    t.ask, a);
    v.AddMember("bid_sz", t.bid_sz, a);
    v.AddMember("ask_sz", t.ask_sz, a);
    v.AddMember("ts_ns",  t.ts_ns, a);
}
